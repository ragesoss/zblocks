#!/usr/bin/env node
// Harvest Wikifunctions' Z60 natural-language catalogue into
// js/languages-data.js. Run manually when you want to refresh the
// picker contents:
//
//   node scripts/build-languages.mjs
//
// Writes a static data file; no runtime dependency on this script.
// Non-RTL languages only (RTL needs a CSS pass before we ship it).
// Each entry gets { iso, native, wfZid } — `iso` from the Z60's
// Z60K1, `native` from its own Z2K3 in its own language (dogfood:
// every language labels itself in its own script), `wfZid` is the
// Z60's ZID.

const WF_API = "https://www.wikifunctions.org/w/api.php";
const OUT = new URL("../js/languages-data.js", import.meta.url);

// Known RTL ISO codes. Filter these out until we do the CSS pass.
// When RTL ships, this set becomes an `rtl: true` flag instead.
const RTL_ISO = new Set([
  "ar", "arc", "arq", "arz", "ary", "azb", "bqi", "ckb", "dv", "fa",
  "glk", "he", "iw", "jrb", "khw", "ks", "ku-arab", "lrc", "mzn",
  "pnb", "ps", "sd", "sdh", "skr", "skr-arab", "syc", "syr", "ug",
  "ur", "yi",
]);

// Popular codes, pinned to the top of the picker in this order.
// Everything else comes alphabetically by native name below them.
// Picked by Wikimedia editor community size, restricted to LTR.
const PINNED_TOP = ["en", "de", "fr", "es", "it", "pt", "ru", "ja", "zh", "pl", "nl", "hi"];

// Seed set — known-good ZIDs for the pinned top languages. The search
// API with empty search ranks arbitrarily and may miss very common
// languages; seeding guarantees they're present.
const SEED_WF_ZIDS = [
  "Z1002", // en
  "Z1430", // de
  "Z1004", // fr
  "Z1003", // es
  "Z1787", // it
  "Z1037", // pt
  "Z1005", // ru
  "Z1830", // ja
  "Z1006", // zh (Chinese)
  "Z1025", // pl (verified against catalog)
  "Z1157", // nl (verified against catalog)
  "Z1820", // hi
];

async function main() {
  console.log("Searching Wikifunctions for Z60 natural-language objects\u2026");
  const searched = await listAllZ60Zids();
  const zids = [...new Set([...SEED_WF_ZIDS, ...searched])];
  console.log(`  ${searched.length} from search + ${SEED_WF_ZIDS.length} seeded = ${zids.length} unique`);
  console.log("Batch-fetching language objects\u2026");
  const langs = [];
  for (const batch of chunks(zids, 50)) {
    const page = await fetchBatch(batch);
    for (const z of batch) {
      const entry = parseLanguageObject(z, page[z]?.wikilambda_fetch);
      if (entry) langs.push(entry);
    }
    process.stdout.write(`  ${langs.length}/${zids.length}\r`);
  }
  console.log(`\n  parsed ${langs.length} entries`);

  // Drop RTL + anything missing an ISO code (typically language
  // variants / constructed languages that don't map cleanly).
  const ltr = langs.filter(l => l.iso && !RTL_ISO.has(l.iso));
  console.log(`  ${ltr.length} after filtering RTL / code-less entries`);

  // Stable ordering: pinned first, then alphabetical by native name
  // (case-insensitive, accent-ignoring).
  const pinned = [];
  for (const iso of PINNED_TOP) {
    const hit = ltr.find(l => l.iso === iso);
    if (hit) pinned.push(hit);
  }
  const rest = ltr
    .filter(l => !PINNED_TOP.includes(l.iso))
    .sort((a, b) => a.native.localeCompare(b.native, "en", { sensitivity: "base" }));

  const ordered = [...pinned, ...rest];

  const out = `// Auto-generated by scripts/build-languages.mjs.
// Re-run that script to refresh. DO NOT edit by hand; your changes
// will be overwritten.
//
// Source: Wikifunctions Z60 catalogue. Generated ${new Date().toISOString()}.
// Entries: ${pinned.length} pinned (top of picker) + ${rest.length} alphabetical.
// RTL languages filtered out until the CSS pass for bidirectional layout.

export const LANGUAGES = ${JSON.stringify(ordered, null, 2)};
`;
  const fs = await import("node:fs/promises");
  await fs.writeFile(OUT, out);
  console.log(`Wrote ${OUT.pathname}`);
}

async function listAllZ60Zids() {
  // wikilambdasearch_labels returns ranked label matches; we iterate
  // with an empty search + paging to capture everything.
  const zids = new Set();
  let continueToken = null;
  for (let i = 0; i < 30; i++) {
    const params = new URLSearchParams({
      action: "query",
      list: "wikilambdasearch_labels",
      wikilambdasearch_search: "",
      wikilambdasearch_type: "Z60",
      wikilambdasearch_language: "en",
      wikilambdasearch_limit: "100",
      format: "json",
      origin: "*",
    });
    if (continueToken) params.set("wikilambdasearch_continue", continueToken);
    const resp = await fetch(`${WF_API}?${params.toString()}`);
    const data = await resp.json();
    const results = data?.query?.wikilambdasearch_labels || [];
    for (const r of results) zids.add(r.page_title);
    const next = data?.continue?.wikilambdasearch_continue;
    if (!next) break;
    continueToken = next;
  }
  return [...zids];
}

async function fetchBatch(zids) {
  const params = new URLSearchParams({
    action: "wikilambda_fetch",
    zids: zids.join("|"),
    format: "json",
    origin: "*",
  });
  const resp = await fetch(`${WF_API}?${params.toString()}`);
  const data = await resp.json();
  if (data.error) throw new Error(`${data.error.code}: ${data.error.info}`);
  return data;
}

function parseLanguageObject(zid, rawStr) {
  if (!rawStr) return null;
  let z2;
  try { z2 = JSON.parse(rawStr); } catch { return null; }
  const body = z2?.Z2K2;
  if (body?.Z1K1 !== "Z60") return null;
  const iso = typeof body.Z60K1 === "string" ? body.Z60K1 : null;
  if (!iso) return null;
  // Native name: prefer a Z11 entry whose Z11K1 points to this very
  // language (the language labelling itself). Fall back to English.
  const entries = z2?.Z2K3?.Z12K1 || [];
  let native = null;
  let english = null;
  for (let i = 1; i < entries.length; i++) {
    const e = entries[i];
    if (!e || typeof e.Z11K2 !== "string") continue;
    if (e.Z11K1 === zid) native = e.Z11K2;
    if (e.Z11K1 === "Z1002") english = e.Z11K2;
  }
  return {
    iso,
    native: native || english || iso,
    english: english || null,
    wfZid: zid,
  };
}

function* chunks(arr, size) {
  for (let i = 0; i < arr.length; i += size) yield arr.slice(i, i + size);
}

main().catch(e => { console.error(e); process.exit(1); });
